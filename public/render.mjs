#!/usr/bin/env node
/**
 * ClaudeLines renderer — spec v1
 *
 * The ONLY executable installed from the registry. Statuslines themselves are
 * inert JSON specs; this file interprets them. Installing a new statusline
 * never installs new code — just a new JSON file.
 *
 * Safety properties (auditable below):
 *   - Never evaluates spec content. Templates are plain text with {variable}
 *     placeholders resolved against a fixed whitelist.
 *   - Strips control characters from specs AND resolved values, so neither a
 *     spec nor a hostile directory name can emit escape sequences. All ANSI
 *     output is generated by this file from validated #rrggbb colors.
 *   - Runs at most two subprocesses, both hardcoded git commands (no shell,
 *     500ms timeout): `git branch --show-current` and `git status --porcelain`
 *     — and only when a spec uses {gitBranch}/{gitDirty}.
 *   - No network access. No writes. Reads only the spec file and stdin.
 *
 * Usage (in ~/.claude/settings.json):
 *   "statusLine": {
 *     "type": "command",
 *     "command": "node ~/.claude/statuslines/render.mjs ~/.claude/statuslines/<slug>.json"
 *   }
 *
 * Test:  node render.mjs <spec.json> --mock
 */

import { readFileSync } from "node:fs";
import { execFileSync } from "node:child_process";
import { basename } from "node:path";
import { homedir } from "node:os";

const SPEC_VERSION = 1;
const RESET = "\x1b[0m";
const ARROW = ""; // powerline right-pointing triangle

// ---------------------------------------------------------------------------
// Spec validation — mirrors the registry's zod schema. A spec that fails any
// check renders a plain warning instead of being partially interpreted.
// ---------------------------------------------------------------------------

const VARIABLES = [
  "model", "dir", "cwd", "gitBranch", "gitDirty", "cost", "duration",
  "apiDuration", "linesAdded", "linesRemoved", "contextPct", "contextLeft",
  "contextBar", "limit5h", "limit7d", "version", "outputStyle", "time",
];
const HEX = /^#[0-9a-fA-F]{6}$/;
const CONTROL = /[\u0000-\u001f\u007f-\u009f]/g;

function cleanText(value, max) {
  if (typeof value !== "string" || value.length > max) return null;
  return value.replace(CONTROL, "") === value ? value : null;
}

function validateSpec(spec) {
  if (!spec || typeof spec !== "object" || spec.version !== SPEC_VERSION) return null;
  if (!Array.isArray(spec.segments) || spec.segments.length < 1 || spec.segments.length > 16) return null;
  const out = { powerline: spec.powerline === true, join: "  ", segments: [] };
  if (spec.join !== undefined) {
    const j = cleanText(spec.join, 8);
    if (j === null) return null;
    out.join = j;
  }
  for (const seg of spec.segments) {
    if (!seg || typeof seg !== "object") return null;
    const text = cleanText(seg.text, 120);
    if (text === null) return null;
    for (const m of text.matchAll(/\{([^}]*)\}/g)) {
      if (!VARIABLES.includes(m[1])) return null;
    }
    for (const c of ["fg", "bg"]) {
      if (seg[c] !== undefined && !HEX.test(seg[c])) return null;
    }
    if (seg.when !== undefined && !VARIABLES.includes(seg.when)) return null;
    out.segments.push({
      text,
      fg: seg.fg,
      bg: seg.bg,
      bold: seg.bold === true,
      dim: seg.dim === true,
      italic: seg.italic === true,
      when: seg.when,
      newline: seg.newline === true,
    });
  }
  return out;
}

// ---------------------------------------------------------------------------
// Variable resolution from Claude Code's statusline stdin JSON.
// https://code.claude.com/docs/en/statusline
// ---------------------------------------------------------------------------

function safeValue(v) {
  return String(v ?? "").replace(CONTROL, "").slice(0, 200);
}

function tilde(p) {
  const home = homedir();
  return p.startsWith(home) ? "~" + p.slice(home.length) : p;
}

function formatDuration(ms) {
  if (!Number.isFinite(ms) || ms <= 0) return "";
  if (ms < 60000) return `${Math.max(1, Math.round(ms / 1000))}s`;
  const m = Math.floor(ms / 60000);
  if (m < 60) return `${m}m`;
  return `${Math.floor(m / 60)}h ${m % 60}m`;
}

function pct(v) {
  return Number.isFinite(v) ? `${Math.round(v)}%` : "";
}

function git(dir, args) {
  try {
    return safeValue(
      execFileSync("git", ["-C", dir, ...args], {
        timeout: 500,
        stdio: ["ignore", "pipe", "ignore"],
        encoding: "utf8",
        maxBuffer: 1024 * 1024,
      }).trim(),
    );
  } catch {
    return "";
  }
}

function resolveVars(input, needs) {
  const dir = input?.workspace?.current_dir || input?.cwd || process.cwd();
  const cost = input?.cost?.total_cost_usd;
  const ctx = input?.context_window ?? {};
  const used = ctx.used_percentage;
  const now = new Date();

  let contextBar = "";
  if (Number.isFinite(used)) {
    const filled = Math.max(0, Math.min(10, Math.round(used / 10)));
    contextBar = "▓".repeat(filled) + "░".repeat(10 - filled);
  }

  return {
    model: safeValue(input?.model?.display_name),
    dir: safeValue(basename(dir)),
    cwd: safeValue(tilde(dir)),
    gitBranch: needs.gitBranch ? git(dir, ["branch", "--show-current"]) : "",
    gitDirty:
      needs.gitDirty && git(dir, ["status", "--porcelain"]) !== "" ? "*" : "",
    cost: Number.isFinite(cost) ? `$${cost.toFixed(2)}` : "",
    duration: formatDuration(input?.cost?.total_duration_ms),
    apiDuration: formatDuration(input?.cost?.total_api_duration_ms),
    linesAdded: Number.isFinite(input?.cost?.total_lines_added)
      ? String(input.cost.total_lines_added) : "",
    linesRemoved: Number.isFinite(input?.cost?.total_lines_removed)
      ? String(input.cost.total_lines_removed) : "",
    contextPct: pct(used),
    contextLeft: pct(ctx.remaining_percentage),
    contextBar,
    limit5h: pct(input?.rate_limits?.five_hour?.used_percentage),
    limit7d: pct(input?.rate_limits?.seven_day?.used_percentage),
    version: safeValue(input?.version),
    outputStyle: safeValue(input?.output_style?.name),
    time: `${String(now.getHours()).padStart(2, "0")}:${String(now.getMinutes()).padStart(2, "0")}`,
  };
}

// ---------------------------------------------------------------------------
// ANSI rendering — the only place escape codes are produced.
// ---------------------------------------------------------------------------

function hexToRgb(hex) {
  return [1, 3, 5].map((i) => parseInt(hex.slice(i, i + 2), 16));
}

function sgr(seg) {
  const codes = [];
  if (seg.bold) codes.push("1");
  if (seg.dim) codes.push("2");
  if (seg.italic) codes.push("3");
  if (seg.fg) codes.push(`38;2;${hexToRgb(seg.fg).join(";")}`);
  if (seg.bg) codes.push(`48;2;${hexToRgb(seg.bg).join(";")}`);
  return codes.length ? `\x1b[${codes.join(";")}m` : "";
}

function renderLine(line, spec) {
  let out = "";
  line.forEach(({ seg, text }, i) => {
    if (spec.powerline) {
      if (i > 0) {
        const prev = line[i - 1].seg.bg;
        const arrowFg = prev ? `38;2;${hexToRgb(prev).join(";")}` : "39";
        const arrowBg = seg.bg ? `48;2;${hexToRgb(seg.bg).join(";")}` : "49";
        out += `${RESET}\x1b[${arrowFg};${arrowBg}m${ARROW}`;
      }
      out += `${RESET}${sgr(seg)} ${text} `;
    } else {
      if (i > 0) out += RESET + spec.join;
      out += sgr(seg) + text + RESET;
    }
  });
  if (spec.powerline) {
    const last = line[line.length - 1].seg.bg;
    out += `${RESET}${last ? `\x1b[38;2;${hexToRgb(last).join(";")}m${ARROW}` : ""}${RESET}`;
  } else {
    out += RESET;
  }
  return out;
}

function render(spec, vars) {
  const lines = [[]];
  for (const seg of spec.segments) {
    if (seg.newline && lines[lines.length - 1].length > 0) lines.push([]);
    if (seg.when && !vars[seg.when]) continue;
    const text = seg.text.replace(/\{([a-zA-Z0-9]+)\}/g, (_, name) => vars[name] ?? "");
    if (!text) continue;
    lines[lines.length - 1].push({ seg, text });
  }
  return lines
    .filter((l) => l.length > 0)
    .map((l) => renderLine(l, spec))
    .join("\n");
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

const args = process.argv.slice(2);
const mock = args.includes("--mock");
const specPath = args.find((a) => !a.startsWith("--"));

if (!specPath) {
  console.log("claudelines: usage: render.mjs <spec.json> [--mock]");
  process.exit(0);
}

let spec = null;
try {
  spec = validateSpec(JSON.parse(readFileSync(specPath, "utf8")));
} catch {
  /* fall through */
}
if (!spec) {
  console.log("⚠ claudelines: invalid or unreadable spec — nothing rendered");
  process.exit(0);
}

const uses = (v) =>
  spec.segments.some((s) => s.when === v || s.text.includes(`{${v}}`));
const needs = { gitBranch: uses("gitBranch"), gitDirty: uses("gitDirty") };

let input = {};
if (mock) {
  input = {
    model: { display_name: "Opus 4.8" },
    workspace: { current_dir: process.cwd() },
    version: "2.1.9",
    output_style: { name: "default" },
    cost: {
      total_cost_usd: 1.42,
      total_duration_ms: 38 * 60000,
      total_api_duration_ms: 12 * 60000,
      total_lines_added: 412,
      total_lines_removed: 97,
    },
    context_window: { used_percentage: 37, remaining_percentage: 63 },
    rate_limits: {
      five_hour: { used_percentage: 24 },
      seven_day: { used_percentage: 41 },
    },
  };
} else {
  try {
    input = JSON.parse(readFileSync(0, "utf8"));
  } catch {
    input = {};
  }
}

console.log(render(spec, resolveVars(input, needs)));
